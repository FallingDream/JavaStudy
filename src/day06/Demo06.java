package day06;
@SuppressWarnings("all")
public class Demo06 {
    public static void main(String[] args) {
        //绝对值
        System.out.println(Math.abs(-18));
        //向上取整
        System.out.println(Math.ceil(15.2));
        //向下取整
        System.out.println(Math.floor(15.2));
        //四舍五入 +0.5再向下取整
        System.out.println(Math.round(15.2));
        //幂
        System.out.println(Math.pow(2,3));
    }
}
